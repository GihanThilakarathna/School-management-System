    private void Update_Table(){
        DefaultTableModel dtm = (DefaultTableModel) jTable2 .getModel();
        dtm.setRowCount(0);
        
        
        try{
            j.con();
            ResultSet  rs = j.getdata("SELECT * FROM `student_fback`");
            
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("feedback_id"));
                v.add(rs.getString("StudentID"));
                v.add(rs.getString("Student_Grade"));
                v.add(rs.getString("feedback"));
                dtm.addRow(v);
                
                
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            e.printStackTrace();
        }
        
    }
