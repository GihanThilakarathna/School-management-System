    private void jButton3SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3SearchActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0);
        
        try{
            j.con();
            ResultSet rs = j.getdata("SELECT * FROM student_fback WHERE feedback_id  ='"+ jTextField4.getText() +"'");
            
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("feedback_id"));
                v.add(rs.getString("StudentID"));
                v.add(rs.getString("Student_Grade"));
                v.add(rs.getString("feedback"));
                dtm.addRow(v);
                
            }
            
            int i = jTable2.getRowCount();
            if(i == 0){
                JOptionPane.showMessageDialog(null,"No information show");
                jTextField4.setText(null);
                
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Sorry! No information to show");
        }
        
        
    }//GEN-LAST:event_jButton3SearchActionPerformed

