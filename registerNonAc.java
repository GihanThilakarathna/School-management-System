     public ArrayList<User02> getUsersList(){
         
         
         
          ArrayList<User02> usersList02 = new ArrayList<User02> ();
         try{
         
         Connection connection = getConnection();
         String query = "SELECT * FROM `staff_register02";
         Statement st;
         ResultSet rs;
             st = connection.createStatement();
             rs = st.executeQuery(query);
             User02 user02;
             while(rs.next())
             {
                 user02 = new User02(rs.getString("reg_num"),rs.getString("f_name"),rs.getString("genderr"),rs.getString("agee"),rs.getString("addresss"),rs.getString("contct_no"),rs.getString("mail"),rs.getString("reg_date01"));
                 usersList02.add(user02);
             }
         }catch(Exception e){
             e.printStackTrace();
         }
         
         return usersList02;
             
         
     }
             
