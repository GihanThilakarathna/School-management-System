    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       
        Connection connection;
        try {
            connection = DBclass.getConnection();
       
        
           PreparedStatement pre1;
     
            pre1 = connection.prepareStatement("select *  from staff_register02 where reg_num= ?   ");
            pre1.setString(1 , regnum1.getText() );
            ResultSet rs = pre1.executeQuery();
            String regnum1 ="" ;
            String fullname1 ="" ;
            String gndr1 ="" ;
            String age011 ="" ;
            String address011 ="" ;
            String contactno1 ="" ;
            String email011 ="" ;
            String dateChooser2 ="" ;
           
         
            while(rs.next())
            {
                regnum1 = rs.getString(1);
                fullname1 = rs.getString(2);
                gndr1  = rs.getString(3);
                age011 = rs.getString(4);
                 address011 = rs.getString(5);
                  contactno1 = rs.getString(6);
                   email011 = rs.getString(7);
                   dateChooser2 = rs.getString(8);
              
            
            
            this.regnum1.setText(regnum1);
            this.fullname1.setText(fullname1);
           this.gndr1.setText(gndr1);
            this.age011.setText(age011);
            this.address011.setText(address011);
            this.contactno1.setText(contactno1);
            this.email011.setText(email011);
            this.dateChooser2.setText(dateChooser2);
           
            }
    }   catch (Exception ex) {
            System.out.println(ex);
        }
                                            
    }//GEN-LAST:event_jButton6ActionPerformed

