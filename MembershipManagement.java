    public  void displayData(){
        
    try{ 
        
    DefaultTableModel Model = new DefaultTableModel(new String[]{"member_id","member_name","class1"},0);    
    
    String sql="SELECT * FROM memberdetails";
     Connection c = DBclass.getConnection();
     Statement stmt = c.createStatement();
    
    ResultSet rs = stmt.executeQuery(sql);
    while(rs.next())
    {
        String a = rs.getString("member_id");
        String b = rs.getString("member_name");
        String C = rs.getString("class1");

        Model.addRow(new Object[]{a,b,C});
    }
    memberdetails.setModel(Model);
    }catch(Exception ex){
    
    }
   
    }//GEN-LAST:event_formWindowOpened

