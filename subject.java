    private void jButton4SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4SearchActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        try{
            j.con();
            ResultSet rs = j.getdata("SELECT * FROM `subject_managment` WHERE  `SubjectID` = '"+jTextField1.getText()+"'");
            while (rs.next()) {                
                Vector v = new Vector();
                v.add(rs.getString("SubjectID"));
                v.add(rs.getString("Subject_Name"));
                v.add(rs.getString("StaffID"));
                v.add(rs.getString("StaffName"));
                v.add(rs.getString("Class"));
                dtm.addRow(v);
            }
            int i = jTable1.getRowCount();
            if(i == 0){
                JOptionPane.showMessageDialog(null,"No information show");
                jTextField1.setText(null);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Sorry ! no information");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4SearchActionPerformed

